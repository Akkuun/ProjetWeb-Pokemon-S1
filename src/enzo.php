                    <h3>Deux derniers critères :</h3>
                    <p>
                        Avec ces deux dernières œuvres, nous pouvons mettre en forme deux derniers critères de valeur
                        pour les
                        œuvres d’art.<br><br>

                        <b>Premièrement</b>, le genre de l’œuvre.<br>
                        En fonction du temps, les <b>genres</b> (principalement au niveau de la peinture) se sont
                        retrouvés <b>plus
                            ou moins influents</b>, si bien que s’en peut <b>modifier</b> leurs <b>prix</b> en fonction
                        de la
                        demande générale.
                        <br><br>
                        <b>Deuxièmement</b>, si l’œuvre est <b>subversive</b>. <br>
                        Au fil de nos recherches nous avons remarqué que les <b>œuvres</b> à caractère
                        <b>subversives</b> ou <b>dénoncent</b>
                        avait une <b>plus valu significative</b> par rapport aux œuvres dite « <b>classique</b> ».<br>
                        Pour illustrer ce <b>dernier critère</b> de valeur, nous avons donc choisi 2 <b>œuvres</b> du
                        <b>même
                            genre</b> (pop art), mais pourtant ayant une <b>valeur significativement éloignée</b>.<br>
                        L’œuvre dite « <b>classique</b> » Est plus jolie, simple, relaxante tandis que l’œuvre
                        subversive elle
                        consiste en un texte écrit sur fond blanc disant : « <i>All of the assholes are gone</i> »
                        <br><br>
                        Malgré le fait que l’<b>œuvre</b> « <b>classique</b> » est globalement plus <b>agréable</b>,
                        c’est
                        l’œuvre <b>subversive</b> qui va afficher le <b>prix</b> le plus <b>onéreux</b>.

                    </p>
